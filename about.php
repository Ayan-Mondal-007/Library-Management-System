        <br>
        <p>Library Management System is designed in such a way that it keeps the functioning of the library smooth and in order. It is user-friendly software. It helps in keeping track of the books, catalogues, magazines, etc. This system increases the efficiency of the librarian and better management of the library. It leads to an easy search of the desired book from the library.

