             Easy way to enter book data
             Full catalogue, circulation, and acquisition system for library stock management
             Web-based OPAC (Online Public Access Catalogue) system
             Simple, clear search interface for all users
             Multilingual and multi-user support
             Export and import records
             A modern integrated library management system
             Print own barcodes
            As we have already discussed the library management system is based on two techniques Bar Code System and RFID System. The bar code system is based on unique bar codes provided to each and every book; it contains all the information about the product. RFID based library management system is a wireless-based system which helps in keeping records in an easy manner. It acts as an anti-theft system, which provides whole security to the Library Management System, there is a facility of alarm system which gets active on stealing of any book from the library.</p>
